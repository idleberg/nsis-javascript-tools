const keywordsLower: Record<string, number> = Object.create(null);
for (const [k, v] of Object.entries(keywords)) {
	keywordsLower[k.toLowerCase()] = v;
}

const isConstant = /^[A-Z][A-Z0-9_]+$/;

export function specializeIdentifier(value: string) {
	return keywords[value] ?? (isConstant.test(value) ? Constant : -1);
}

export function specializeIdentifierLoose(value: string) {
	return keywordsLower[value.toLowerCase()] ?? (isConstant.test(value) ? Constant : -1);
}
